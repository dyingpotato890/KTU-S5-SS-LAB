DATA SEGMENT
    ISEVEN DB 10,13,'THE NUMBER IS EVEN$'
    ISODD DB 10,13,'THE NUMBER IS ODD$'
    NUM DW 345EH
DATA ENDS
CODE SEGMENT
    ASSUME CS:CODE, DS:DATA

START:    
    MOV AX,DATA
    MOV AX,DATA
    MOV DS,AX

    LEA SI,NUM
    MOV AX,[SI]

    RCR AX,1
    JNC EVENNO
ODDNO:
    LEA DX,ISODD
    JMP PRINT
EVENNO:
    LEA DX,ISEVEN
PRINT:
    MOV AH,09H
    INT 21H

    MOV AH,4CH
    INT 21H
CODE ENDS
END START